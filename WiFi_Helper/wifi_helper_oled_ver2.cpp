void WiFiOledHelper::loadingProgressBar()
{
  display.clear();
  display.setTextAlignment(TEXT_ALIGN_CENTER);
  display.setFont(Irish_Grover_Regular_14);
  display.drawString(64, 8, "We - WiFi Helper");
  display.setFont(Irish_Grover_Regular_12);
  int progress = (counter / 5) % 100;
  display.setTextAlignment(TEXT_ALIGN_CENTER);
  // x , y ,with, height
  display.drawProgressBar(4, 32, 120, 10, progress);
  display.setTextAlignment(TEXT_ALIGN_CENTER);
  display.drawString(64, 48, "Loading....");
  display.display();
  if (millis() - timeSinceLastModeSwitch > 100)
  {
    timeSinceLastModeSwitch = millis();
    counter++;
  }
}

